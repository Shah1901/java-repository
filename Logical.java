
public class Logical {
    public static void main(String[] args) {
        System.out.println("Logical Operators");
        System.out.println((10>5) && (4>2));
        System.out.println((60<8) || (40<20));
        System.out.println((10!=9));
        System.out.println(!(10>5));
        System.out.println(!(10<5));
    }
}
